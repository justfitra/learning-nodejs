const os = require("os");

// Tipe OS
console.log("Tipe OS:", os.type());

// Platform OS
console.log("Platform:", os.platform());

// Total RAM
console.log("Total RAM:", os.totalmem());

// Sisa RAM
console.log("Free RAM:", os.freemem());

// Direktori Home User
console.log("Home Directory:", os.homedir());
